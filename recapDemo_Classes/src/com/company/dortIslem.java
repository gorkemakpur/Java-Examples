package com.company;

public class dortIslem {
    public double topla(double a,double b)
    {
        return a+b;
    }

    public double cikar(double a,double b)
    {
        return a-b;
    }

    public double carp(double a,double b)
    {
        return a*b;
    }

    public double bol(double a,double b)
    {
        return a/b;
    }

}
